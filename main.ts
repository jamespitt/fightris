function desvioV (sprite: Sprite) {
    if (sprite.x < sombra_list[sprite_list.indexOf(sprite)].x && sprite.y == sombra_list[sprite_list.indexOf(sprite)].y) {
        spriteHorizontal[sprite_list.indexOf(sprite)] = -1
        spriteVertical[sprite_list.indexOf(sprite)] = 0
        sprite.setImage(posicionesPlayer[sprite_list.indexOf(sprite)][3])
    } else if (sprite.x > sombra_list[sprite_list.indexOf(sprite)].x && sprite.y == sombra_list[sprite_list.indexOf(sprite)].y) {
        spriteHorizontal[sprite_list.indexOf(sprite)] = 1
        spriteVertical[sprite_list.indexOf(sprite)] = 0
        sprite.setImage(posicionesPlayer[sprite_list.indexOf(sprite)][2])
    }
}
