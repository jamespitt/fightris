    levelTexto2 = textsprite.create("Time Bonus: " + convertToText(statusBarTiempo.value) + "x" + convertToText(nroNivel + 1) + "=" + convertToText(bonusPoints), 0, 7)
    levelTexto2.setPosition(80, 55)
    levelTexto2.setMaxFontHeight(2)
    energyPoints = statusBarEnergia.value * (nroNivel + 1)
    gameScore += energyPoints
    levelTexto3 = textsprite.create("Energy Bonus: " + convertToText(statusBarEnergia.value) + "x" + convertToText(nroNivel + 1) + "=" + convertToText(energyPoints), 0, 5)
    levelTexto3.setPosition(80, 65)
    levelTexto3.setMaxFontHeight(2)
    levelTexto4 = textsprite.create("Total Points: " + convertToText(gameScore), 0, 1)
    levelTexto4.setPosition(80, 90)
    levelTexto4.setMaxFontHeight(8)
